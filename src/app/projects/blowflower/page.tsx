// Noise function taken from https://www.reddit.com/r/elementor/comments/18500xe/how_can_i_add_a_background_grain_effect_on_my/
const noise = () => {
  let canvas: HTMLCanvasElement, ctx: CanvasRenderingContext2D;
  let wWidth: number, wHeight: number;
  let noiseData: ImageData[] = [];
  let frame = 0;
  let loopTimeout: number;
  const createNoise = () => {
    const idata = ctx.createImageData(wWidth, wHeight);
    const buffer32 = new Uint32Array(idata.data.buffer);
    const len = buffer32.length;

    for (let i = 0; i < len; i++) {
      if (Math.random() < 0.5) {
        buffer32[i] = 0xff000000;
      }
    }

    noiseData.push(idata);
  };

  const paintNoise = (f: number) => {
    if (f === 9) {
      frame = 0;
    } else {
      frame++;
    }

    ctx.putImageData(noiseData[f], 0, 0);
  };

  const loop = () => {
    paintNoise(frame);
    // requestAnimationFrame(loop);
    loopTimeout = window.setTimeout(() => {
      window.requestAnimationFrame(loop);
    }, 1000 / 3); //how quickly the noise happens
  };

  // Setup
  const setup = () => {
    wWidth = window.innerWidth;
    wHeight = window.innerHeight;

    canvas.width = wWidth;
    canvas.height = wHeight;

    for (let i = 0; i < 10; i++) {
      createNoise();
    }

    loop();
  };

  // Reset
  let resizeThrottle: number;
  const reset = () => {
    window.addEventListener(
      "resize",
      () => {
        window.clearTimeout(resizeThrottle);

        resizeThrottle = window.setTimeout(() => {
          window.clearTimeout(loopTimeout);
          setup();
        }, 200);
      },
      false
    );
  };

  // Init
  const init = (() => {
    canvas = document.getElementById("noise") as HTMLCanvasElement;
    ctx = canvas.getContext("2d")!;
    setup();
  })();
};

